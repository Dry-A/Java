package vetorEArray;

public class LengthStartsEnds {
    public static void main(String[] args) {

        String descricao = new String("Pera Williams, a uva mais doce do mercado!");

        System.out.println(descricao.length());

        System.out.println(descricao.startsWith("Pera"));
        System.out.println(descricao.endsWith("!"));




    }
}
