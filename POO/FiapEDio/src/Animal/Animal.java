package Animal;

public class Animal {

   private String locomove;
   private String alimentar;


    public String getLocomove() {
        return locomove;
    }

    public void setLocomove(String locomove) {
        this.locomove = locomove;
    }

    public String getAlimentar() {
        return alimentar;
    }

    public void setAlimentar(String alimentar) {
        this.alimentar = alimentar;
    }


}
